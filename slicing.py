names = ['Caroline', 'Rocklyn', 'Roselyn', 'Charles', 'Caleb', 'Adam']
print(names[1:4])
#just like range the last element isn't added just the one right before it

print(names[:4])
print(names[1:])
#omitting the first will automatically print from the beginning
#an vice versa works for the last 

print(names[-3:])
#This prints the names of the last three players